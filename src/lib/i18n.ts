export function normalizeLanguage(value?: string | null): AppLanguage {
  if (value === "fr" || value === "ar") {
    return value;
  }

  return "en";
}

export function getLanguageDirection(language: AppLanguage) {
  return language === "ar" ? "rtl" : "ltr";
}

